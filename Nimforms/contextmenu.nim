proc newContextMenu*(parent: TrayIcon, menuNames: varargs[string, `$`]): ContextMenu =
    result = ContextMenu.init()
    result.mTray = parent 
    result.mTrayParent = true   
    result.mFont = newFont("Tahoma", 11)    
    result.createMsgWindow()
